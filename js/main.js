///////////////////////////////////////////////////////////////////// scrolling header///////////////////////////////////////////////////////////////////////////////////
window.addEventListener('scroll', function() {
    var mobileAnv = document.querySelector('.containt .mobileanv');
    if (window.scrollY > 100) { // Change 100 to the desired scroll position
        mobileAnv.classList.add('fixed');
    } else {
        mobileAnv.classList.remove('fixed');
    }
});

